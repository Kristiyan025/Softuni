using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Exs_02_Numbers_N_1
{
    class Program
    {
        static void Main(string[] args)
        {

            int a = int.Parse(Console.ReadLine());
            for (int i = a; i >= 1; i--)
            {
                Console.WriteLine(i);
            }

        }
    }
}
